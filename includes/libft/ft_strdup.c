#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*a;
	int		i;

	a = malloc((ft_strlen(s1) + 1) * sizeof(*s1));
	if (a == NULL)
		return (0);
	i = 0;
	while (s1[i] != '\0')
	{
		a[i] = s1[i];
		i++;
	}
	a[i] = '\0';
	return (a);
}
